// Auto show .
// 自动滚动显示，FIXEDDISPLAY 固定显示行 SCREENROWS 屏幕的总行数
// 显示方式：
// ---固定行---  ->  ---固定行---  ->  ---固定行---   ->  ---固定行---  
// 1                1                1                 2
//                  2                2                 3
//                                   3                 4
//

void OLED::show(char *s, uint8_t c) {
  if(lineNumber<=SCREENROWS-FIXEDDISPLAY){
    clear_line(lineNumber);
    sendStrXY(s, lineNumber, c);

    if(lineNumber > 1){
      lineContent[lineNumber-2] = s;
    }
    lineNumber++;
  }
  else{
    for(int i=FIXEDDISPLAY,j=0;i<lineNumber-1;i++,j++){
      //clear_line(i);
      sendStrXY(lineContent[j].c_str(),i,c);
    }
    //clear_line(lineNumber-1);
    sendStrXY(s, lineNumber-1, c);
    for(int k=0;k<(lineNumber-2)/2;k++){
      lineContent[k] = lineContent[k+1];
    }
    lineContent[lineNumber-3] = s;
  }
}

void OLED::show(int number, uint8_t c){
    String str = (String)number;
    show(const_cast<char*>(str.c_str()),c);
}
