OLED::OLED(uint8_t sda, uint8_t scl, uint8_t address, uint8_t offset) {
	_sda = sda;
	_scl = scl;
	_address = address;
	_offset = offset;
}

void OLED::begin(void) {
	// set up i2c
	Wire.begin(_sda, _scl);
	init_OLED();
	reset_display();
}

void OLED::on(void) {
	DEBUG_PRINTLN("on");
	displayOn();
}

void OLED::off(void) {
	DEBUG_PRINTLN("off");
	displayOff();
}

void OLED::clear(void) {
	DEBUG_PRINTLN("clear");
	clear_display();
}

